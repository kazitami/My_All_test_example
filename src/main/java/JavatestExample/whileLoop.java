package JavatestExample;

public class whileLoop {

	public static void main(String[] args) {
		int i=1;
		while(i<6)
		{
			System.out.print(i + " ");
			i++;
		}
		
		System.out.println();
		
		int b=1;
		while(b<=100) {
			System.out.print(b + " ");
			b++;
		}
		
		
		System.out.println();
		int c=100;
		while(c>=1) {
			System.out.print(c + " ");
			c--;
		}
		
		
		System.out.println();
		int d=1;
		while(d<=20)
		{
			System.out.print(d*5 +" ");
			d++;
		}
		
		System.out.println();
		int k=1;
		while(k<=1083) {
			System.out.print(k*97 +" ");
			k++;
		}
		
	}

}
