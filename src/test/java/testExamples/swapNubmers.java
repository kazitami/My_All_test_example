package testExamples;

public class swapNubmers {

	public static void main(String[] args) {
		//To  swap two nubers without using third number/variable
		int a=5;
		int b=10;
		
		a=a+b;// a is 15
		b=a-b;//b is 5
		a=a-b;//a is 10
		System.out.println("a----" + a);
		System.out.println("b----" + b);

	}

}
